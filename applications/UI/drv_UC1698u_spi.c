void UC1698u_spi_Write(void* data, uint16_t len)
{
    rt_pin_write(LCD_CS, PIN_LOW);
    HAL_SPI_Transmit(&hspi2, data, len, 1000);
    rt_pin_write(LCD_CS, PIN_HIGH);
}



